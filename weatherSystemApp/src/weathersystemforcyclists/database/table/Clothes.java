package weathersystemforcyclists.database.table;

public class Clothes {
	
	private int clothesID;
	private int comfortRate;
	private String headClothes;
	private	String thoraxClothes;
	private String handsClothes;
	private String legsClothes;
	private String feetClothes;
	private String comment;
	
	
	
	public int getClothesID() {
		return clothesID;
	}
	public void setClothesID(int clothesID) {
		this.clothesID = clothesID;
	}
	public int getComfortRate() {
		return comfortRate;
	}
	public void setComfortRate(int comfortRate) {
		this.comfortRate = comfortRate;
	}
	public String getHeadClothes() {
		return headClothes;
	}
	public void setHeadClothes(String headClothes) {
		this.headClothes = headClothes;
	}
	public String getThoraxClothes() {
		return thoraxClothes;
	}
	public void setThoraxClothes(String thoraxClothes) {
		this.thoraxClothes = thoraxClothes;
	}
	public String getHandsClothes() {
		return handsClothes;
	}
	public void setHandsClothes(String handsClothes) {
		this.handsClothes = handsClothes;
	}
	public String getLegsClothes() {
		return legsClothes;
	}
	public void setLegsClothes(String legsClothes) {
		this.legsClothes = legsClothes;
	}
	public String getFeetClothes() {
		return feetClothes;
	}
	public void setFeetClothes(String feetClothes) {
		this.feetClothes = feetClothes;
	}
	public String getComment() {
		return comment;
	}
	public void setComment(String comment) {
		this.comment = comment;
	}
}
